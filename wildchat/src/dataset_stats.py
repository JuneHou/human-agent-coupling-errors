"""Step 1 — statistical characterization of WildChat-1M and ShareChat.

Streams the raw files, computes per-dataset descriptive stats, and writes:
  docs/dataset-stats.json   (machine-readable)
  docs/dataset-stats.md     (human-readable summary)

Run:  python3 src/dataset_stats.py
"""
from __future__ import annotations
import json
from collections import Counter
from pathlib import Path
from statistics import median

import numpy as np

from load import (REPO, SHARECHAT_PLATFORMS, iter_wildchat, iter_sharechat_rows,
                  sharechat_header)


def pctiles(vals):
    a = np.asarray(vals, dtype=float)
    return {
        "n": int(a.size),
        "mean": round(float(a.mean()), 3),
        "median": float(np.median(a)),
        "p90": float(np.percentile(a, 90)),
        "p99": float(np.percentile(a, 99)),
        "max": float(a.max()),
        "multi_turn_pct": round(float((a > 1).mean()) * 100, 2),
    }


def topn(counter: Counter, n=12):
    tot = sum(counter.values()) or 1
    return [(k, v, round(v / tot * 100, 2)) for k, v in counter.most_common(n)]


# ─────────────────────────── WildChat ───────────────────────────
def wildchat_stats():
    turns, langs, models = [], Counter(), Counter()
    tmin = tmax = None
    n = 0
    for b in iter_wildchat(columns=["turn", "language", "model", "timestamp"]):
        for t in b["turn"]:
            turns.append(t)
        langs.update(b["language"])
        models.update(b["model"])
        for ts in b["timestamp"]:
            if ts is None:
                continue
            tmin = ts if tmin is None or ts < tmin else tmin
            tmax = ts if tmax is None or ts > tmax else tmax
        n += len(b["turn"])
    return {
        "n_conversations": n,
        "n_messages_est": int(sum(turns) * 2),  # turn = user+assistant rounds
        "turns_per_conversation": pctiles(turns),
        "languages_top": topn(langs),
        "n_languages": len(langs),
        "models_top": topn(models),
        "time_range": [str(tmin), str(tmax)],
    }


# ─────────────────────────── ShareChat ───────────────────────────
def sharechat_platform_stats(platform: str):
    header = set(sharechat_header(platform))
    has_thinking = "thinking" in header
    has_topic = "topic" in header
    lang_col = "detected_language_final"

    n_msg = 0
    urls = {}                      # url -> turns_count (int)
    langs, topics, roles = Counter(), Counter(), Counter()
    assistant_msgs = 0
    thinking_nonempty = 0

    for row in iter_sharechat_rows(platform):
        n_msg += 1
        url = row.get("url", "")
        tc = row.get("turns_count", "")
        try:
            urls[url] = int(float(tc))
        except (ValueError, TypeError):
            urls.setdefault(url, None)
        roles.update([row.get("role", "")])
        if lang_col in row:
            langs.update([row.get(lang_col) or "unknown"])
        if has_topic:
            tp = (row.get("topic") or "").strip()
            if tp:                       # topic is only stored on user rows
                topics.update([tp])
        if row.get("role") == "llm":     # all platforms label the AI turn "llm"
            assistant_msgs += 1
            if has_thinking and (row.get("thinking") or "").strip():
                thinking_nonempty += 1

    tc_vals = [v for v in urls.values() if v is not None]
    return {
        "platform": platform,
        "n_messages": n_msg,
        "n_conversations": len(urls),
        "turns_per_conversation": pctiles(tc_vals) if tc_vals else None,
        "roles": dict(roles),
        "has_thinking_column": has_thinking,
        "assistant_messages": assistant_msgs,
        "assistant_with_thinking": thinking_nonempty,
        "thinking_coverage_pct": (round(thinking_nonempty / assistant_msgs * 100, 2)
                                  if has_thinking and assistant_msgs else None),
        "languages_top": topn(langs),
        "n_languages": len(langs),
        "topics_top": topn(topics) if has_topic else None,
    }


def sharechat_stats():
    per = {p: sharechat_platform_stats(p) for p in SHARECHAT_PLATFORMS}
    total_conv = sum(v["n_conversations"] for v in per.values())
    total_msg = sum(v["n_messages"] for v in per.values())
    return {"total_conversations": total_conv, "total_messages": total_msg,
            "per_platform": per}


# ─────────────────────────── reporting ───────────────────────────
def fmt_topn(rows):
    return ", ".join(f"{k} ({pct}%)" for k, _, pct in rows) if rows else "—"


def write_markdown(wc, sc, out: Path):
    L = []
    L.append("# Dataset statistics — WildChat-1M and ShareChat\n")
    L.append("_Auto-generated by `src/dataset_stats.py`. Step 1 of the Phase-1 plan._\n")

    L.append("## WildChat-1M (one row per conversation)\n")
    L.append(f"- **Conversations:** {wc['n_conversations']:,}")
    L.append(f"- **Est. messages:** {wc['n_messages_est']:,} (turn × 2)")
    tp = wc["turns_per_conversation"]
    L.append(f"- **Turns/conversation:** mean {tp['mean']}, median {tp['median']:.0f}, "
             f"p90 {tp['p90']:.0f}, p99 {tp['p99']:.0f}, max {tp['max']:.0f}")
    L.append(f"- **Multi-turn (>1 round):** {tp['multi_turn_pct']}%")
    L.append(f"- **Languages:** {wc['n_languages']} distinct; top: {fmt_topn(wc['languages_top'])}")
    L.append(f"- **Models:** {fmt_topn(wc['models_top'])}")
    L.append(f"- **Time range:** {wc['time_range'][0]} → {wc['time_range'][1]}\n")

    L.append("## ShareChat (one row per message; conversation = `url`)\n")
    L.append(f"- **Conversations (all platforms):** {sc['total_conversations']:,}")
    L.append(f"- **Messages (all platforms):** {sc['total_messages']:,}\n")
    L.append("| Platform | Convs | Msgs | Turns/conv (med, p90) | Thinking col | Thinking cov. | Langs | Top topics |")
    L.append("|---|--:|--:|---|:--:|--:|--:|---|")
    for p in SHARECHAT_PLATFORMS:
        v = sc["per_platform"][p]
        tp = v["turns_per_conversation"]
        tps = f"{tp['median']:.0f}, {tp['p90']:.0f}" if tp else "—"
        thc = f"{v['thinking_coverage_pct']}%" if v["thinking_coverage_pct"] is not None else "—"
        topics = fmt_topn(v["topics_top"][:3]) if v["topics_top"] else "—"
        L.append(f"| {p} | {v['n_conversations']:,} | {v['n_messages']:,} | {tps} | "
                 f"{'yes' if v['has_thinking_column'] else 'no'} | {thc} | {v['n_languages']} | {topics} |")
    L.append("")
    L.append("> **Thinking coverage** = share of assistant messages with a non-empty `thinking` "
             "field — the AI-internal-state signal that WildChat lacks (relevant to the AI→Human axis).\n")
    out.write_text("\n".join(L))


def compute():
    """Pure: characterize both corpora and return {wildchat, sharechat} (no file write).

    HEAVY — reads the full WildChat parquet shards + the raw ShareChat CSVs. For a fast,
    non-recomputing view, load the cached docs/dataset-stats.json instead.
    """
    return {"wildchat": wildchat_stats(), "sharechat": sharechat_stats()}


def main():
    docs = REPO / "docs"
    docs.mkdir(exist_ok=True)
    print("Computing WildChat stats…", flush=True)
    res = compute()
    wc, sc = res["wildchat"], res["sharechat"]
    print(f"  WildChat: {wc['n_conversations']:,} conversations", flush=True)
    print(f"  ShareChat: {sc['total_conversations']:,} conversations / "
          f"{sc['total_messages']:,} messages", flush=True)
    (docs / "dataset-stats.json").write_text(json.dumps(res, indent=2))
    write_markdown(wc, sc, docs / "dataset-stats.md")
    print("Wrote docs/dataset-stats.json and docs/dataset-stats.md", flush=True)


if __name__ == "__main__":
    main()
